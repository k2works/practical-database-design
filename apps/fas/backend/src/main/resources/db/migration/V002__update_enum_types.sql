-- 財務会計システム ENUM 型更新
-- chapter15: 勘定科目の設計

-- 取引要素区分の追加
CREATE TYPE "取引要素区分" AS ENUM ('資産', '負債', '資本', '収益', '費用');

-- 既存の集計区分を削除して再作成（V001 では '集計', '明細' だったが、正しくは以下）
DROP TYPE IF EXISTS "集計区分" CASCADE;
CREATE TYPE "集計区分" AS ENUM ('見出科目', '集計科目', '計上科目');

-- 消費税計算区分の追加（課税区分とは別に定義）
-- 注: V001 で定義済みの課税区分は仕訳用。消費税計算区分は勘定科目用。
CREATE TYPE "消費税計算区分" AS ENUM ('課税', '非課税', '免税', '対象外');
